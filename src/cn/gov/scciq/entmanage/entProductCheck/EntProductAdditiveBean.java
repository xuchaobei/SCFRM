package cn.gov.scciq.entmanage.entProductCheck;

public class EntProductAdditiveBean {
	private String entAdditiveID;
	private String entProductInputID;
	private String additiveName;
	private String purpose;
	private String useProcess;
	private String useQuantity;
	public EntProductAdditiveBean() {
		super();
	}
	public String getEntAdditiveID() {
		return entAdditiveID;
	}
	public void setEntAdditiveID(String entAdditiveID) {
		this.entAdditiveID = entAdditiveID;
	}
	public String getEntProductInputID() {
		return entProductInputID;
	}
	public void setEntProductInputID(String entProductInputID) {
		this.entProductInputID = entProductInputID;
	}
	public String getAdditiveName() {
		return additiveName;
	}
	public void setAdditiveName(String additiveName) {
		this.additiveName = additiveName;
	}
	public String getPurpose() {
		return purpose;
	}
	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}
	public String getUseProcess() {
		return useProcess;
	}
	public void setUseProcess(String useProcess) {
		this.useProcess = useProcess;
	}
	public String getUseQuantity() {
		return useQuantity;
	}
	public void setUseQuantity(String useQuantity) {
		this.useQuantity = useQuantity;
	}
	


}
