package cn.gov.scciq.entmanage.entProductCheck;

public class ProductMaterialBean {
	private String productMaterialID;
	private String productCode;
	private String materialClassCode;
	private String materialSubclassCode;
	private String materialCode;
	private String materialSourceCode;
	private String ifMainMaterial;
	
	public ProductMaterialBean() {
		super();
	}
	public String getProductMaterialID() {
		return productMaterialID;
	}
	public void setProductMaterialID(String productMaterialID) {
		this.productMaterialID = productMaterialID;
	}
	public String getProductCode() {
		return productCode;
	}
	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}
	public String getMaterialClassCode() {
		return materialClassCode;
	}
	public void setMaterialClassCode(String materialClassCode) {
		this.materialClassCode = materialClassCode;
	}
	public String getMaterialSubclassCode() {
		return materialSubclassCode;
	}
	public void setMaterialSubclassCode(String materialSubclassCode) {
		this.materialSubclassCode = materialSubclassCode;
	}
	public String getMaterialCode() {
		return materialCode;
	}
	public void setMaterialCode(String materialCode) {
		this.materialCode = materialCode;
	}
	public String getMaterialSourceCode() {
		return materialSourceCode;
	}
	public void setMaterialSourceCode(String materialSourceCode) {
		this.materialSourceCode = materialSourceCode;
	}
	public String getIfMainMaterial() {
		return ifMainMaterial;
	}
	public void setIfMainMaterial(String ifMainMaterial) {
		this.ifMainMaterial = ifMainMaterial;
	}
	


}
