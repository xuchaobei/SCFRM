package cn.gov.scciq.entmanage.entProductCheck;

public class EntProductAccessoryBean {
	
	private String entAccessoryID;
	private String entProductInputID;
	private String accessoryName;
	private String accessoryUse;
	private String usedProcess;
	public EntProductAccessoryBean() {
		super();
	}
	public String getEntAccessoryID() {
		return entAccessoryID;
	}
	public void setEntAccessoryID(String entAccessoryID) {
		this.entAccessoryID = entAccessoryID;
	}
	public String getEntProductInputID() {
		return entProductInputID;
	}
	public void setEntProductInputID(String entProductInputID) {
		this.entProductInputID = entProductInputID;
	}
	public String getAccessoryName() {
		return accessoryName;
	}
	public void setAccessoryName(String accessoryName) {
		this.accessoryName = accessoryName;
	}
	public String getAccessoryUse() {
		return accessoryUse;
	}
	public void setAccessoryUse(String accessoryUse) {
		this.accessoryUse = accessoryUse;
	}
	public String getUsedProcess() {
		return usedProcess;
	}
	public void setUsedProcess(String usedProcess) {
		this.usedProcess = usedProcess;
	}
	

}
