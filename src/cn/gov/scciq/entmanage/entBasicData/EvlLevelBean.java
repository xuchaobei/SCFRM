package cn.gov.scciq.entmanage.entBasicData;

public class EvlLevelBean {
	private String evlLevelID;
	private String levelType;
	private String levelDesc;
	private String definedLevel;
	private String remarks;
	private String weight;
	private String productSamplingFactorFlg;
	private String itemSamplingFactorFlg;
	public EvlLevelBean() {
		super();
	}
	public String getEvlLevelID() {
		return evlLevelID;
	}
	public void setEvlLevelID(String evlLevelID) {
		this.evlLevelID = evlLevelID;
	}
	public String getLevelType() {
		return levelType;
	}
	public void setLevelType(String levelType) {
		this.levelType = levelType;
	}
	public String getLevelDesc() {
		return levelDesc;
	}
	public void setLevelDesc(String levelDesc) {
		this.levelDesc = levelDesc;
	}
	public String getDefinedLevel() {
		return definedLevel;
	}
	public void setDefinedLevel(String definedLevel) {
		this.definedLevel = definedLevel;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getProductSamplingFactorFlg() {
		return productSamplingFactorFlg;
	}
	public void setProductSamplingFactorFlg(String productSamplingFactorFlg) {
		this.productSamplingFactorFlg = productSamplingFactorFlg;
	}
	public String getItemSamplingFactorFlg() {
		return itemSamplingFactorFlg;
	}
	public void setItemSamplingFactorFlg(String itemSamplingFactorFlg) {
		this.itemSamplingFactorFlg = itemSamplingFactorFlg;
	}

}
