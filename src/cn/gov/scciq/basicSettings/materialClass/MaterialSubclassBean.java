package cn.gov.scciq.basicSettings.materialClass;

public class MaterialSubclassBean {

	/**
	 * @param args
	 */
	private String materialSubclassID;
	private String classCode;
	private String subclassCode;
	private String subclassName;
	public String getMaterialSubclassID() {
		return materialSubclassID;
	}
	public void setMaterialSubclassID(String materialSubclassID) {
		this.materialSubclassID = materialSubclassID;
	}
	public String getClassCode() {
		return classCode;
	}
	public void setClassCode(String classCode) {
		this.classCode = classCode;
	}
	public String getSubclassCode() {
		return subclassCode;
	}
	public void setSubclassCode(String subclassCode) {
		this.subclassCode = subclassCode;
	}
	public String getSubclassName() {
		return subclassName;
	}
	public void setSubclassName(String subclassName) {
		this.subclassName = subclassName;
	}
	public MaterialSubclassBean() {
		super();
	}

}
