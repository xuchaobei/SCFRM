package cn.gov.scciq.basicSettings.intendedUse;

public class IntendUseSubBean {
	private String intendedUseSubID;
	private String useCode;
    private String useCodeSub;
    private String useName;
	public IntendUseSubBean() {
		super();
	}
	public String getIntendedUseSubID() {
		return intendedUseSubID;
	}
	public void setIntendedUseSubID(String intendedUseSubID) {
		this.intendedUseSubID = intendedUseSubID;
	}
	public String getUseCode() {
		return useCode;
	}
	public void setUseCode(String useCode) {
		this.useCode = useCode;
	}
	public String getUseCodeSub() {
		return useCodeSub;
	}
	public void setUseCodeSub(String useCodeSub) {
		this.useCodeSub = useCodeSub;
	}
	public String getUseName() {
		return useName;
	}
	public void setUseName(String useName) {
		this.useName = useName;
	}
    
}
