package cn.gov.scciq.basicSettings.item;

public class ItemBean {
     private String itemID;
     private String itemCode;
     private String itemName;
     private String riskClassCode;
     private String itemType;
     private String labFlg;
     private String ifSet;
     private String lrPItemCode;
     private String lrPItemName;
    private String  className;
    
	public ItemBean() {
		super();
	}
	public String getItemID() {
		return itemID;
	}
	public void setItemID(String itemID) {
		this.itemID = itemID;
	}
	public String getItemCode() {
		return itemCode;
	}
	public void setItemCode(String itemCode) {
		this.itemCode = itemCode;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getRiskClassCode() {
		return riskClassCode;
	}
	public void setRiskClassCode(String riskClassCode) {
		this.riskClassCode = riskClassCode;
	}

	public String getLrPItemCode() {
		return lrPItemCode;
	}
	public void setLrPItemCode(String lrPItemCode) {
		this.lrPItemCode = lrPItemCode;
	}
	public String getLrPItemName() {
		return lrPItemName;
	}
	public void setLrPItemName(String lrPItemName) {
		this.lrPItemName = lrPItemName;
	}
	public String getItemType() {
		return itemType;
	}
	public void setItemType(String itemType) {
		this.itemType = itemType;
	}
	public String getLabFlg() {
		return labFlg;
	}
	public void setLabFlg(String labFlg) {
		this.labFlg = labFlg;
	}
	public String getIfSet() {
		return ifSet;
	}
	public void setIfSet(String ifSet) {
		this.ifSet = ifSet;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className){
		this.className = className;
	}
	
	
	
     
  
}
