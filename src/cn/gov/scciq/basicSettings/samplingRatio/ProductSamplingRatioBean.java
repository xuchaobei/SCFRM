package cn.gov.scciq.basicSettings.samplingRatio;

public class ProductSamplingRatioBean {
	
	private String productSamplingRatioID;
	private String levelDesc;
	private String samplingLevel;
	private String lowerSamplingRatio;
	private String upperSamplingRatio;
	public String getProductSamplingRatioID() {
		return productSamplingRatioID;
	}
	public void setProductSamplingRatioID(String productSamplingRatioID) {
		this.productSamplingRatioID = productSamplingRatioID;
	}
	public String getLevelDesc() {
		return levelDesc;
	}
	public void setLevelDesc(String levelDesc) {
		this.levelDesc = levelDesc;
	}
	public String getSamplingLevel() {
		return samplingLevel;
	}
	public void setSamplingLevel(String samplingLevel) {
		this.samplingLevel = samplingLevel;
	}
	public String getLowerSamplingRatio() {
		return lowerSamplingRatio;
	}
	public void setLowerSamplingRatio(String lowerSamplingRatio) {
		this.lowerSamplingRatio = lowerSamplingRatio;
	}
	public String getUpperSamplingRatio() {
		return upperSamplingRatio;
	}
	public void setUpperSamplingRatio(String upperSamplingRatio) {
		this.upperSamplingRatio = upperSamplingRatio;
	}
	public ProductSamplingRatioBean() {
		super();
	}
	

}
