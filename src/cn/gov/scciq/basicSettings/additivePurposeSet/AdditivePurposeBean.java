public class AdditivePurposeBean {
	private String additivePurposeID;
	private String purpose;
	private String addtiveTypeID;
	public String getAdditivePurposeID() {
		return additivePurposeID;
	}
	public void setAdditivePurposeID(String additivePurposeID) {
		this.additivePurposeID = additivePurposeID;
	}
	public String getPurpose() {
		return purpose;
	}
	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}
	public String getAddtiveTypeID() {
		return addtiveTypeID;
	}
	public void setAddtiveTypeID(String addtiveTypeID) {
		this.addtiveTypeID = addtiveTypeID;
	}
	public AdditivePurposeBean() {
		super();
	}

}
