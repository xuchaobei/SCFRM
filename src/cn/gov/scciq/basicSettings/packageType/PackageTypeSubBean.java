package cn.gov.scciq.basicSettings.packageType;

public class PackageTypeSubBean {
	private String packageTypeSubID;
	private String typeCode;
	private String typeCodeSub;
	private String typeName;
	public PackageTypeSubBean() {
		super();
	}
	public String getPackageTypeSubID() {
		return packageTypeSubID;
	}
	public void setPackageTypeSubID(String packageTypeSubID) {
		this.packageTypeSubID = packageTypeSubID;
	}
	public String getTypeCode() {
		return typeCode;
	}
	public void setTypeCode(String typeCode) {
		this.typeCode = typeCode;
	}
	public String getTypeCodeSub() {
		return typeCodeSub;
	}
	public void setTypeCodeSub(String typeCodeSub) {
		this.typeCodeSub = typeCodeSub;
	}
	public String getTypeName() {
		return typeName;
	}
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	
}
