package cn.gov.scciq.basicSettings.countryArea;

public class CountryAreaSetBean {
	private String countryID;
	private String countryCode;
	private String countryName;
	private String ifSet;
	private String countrySubID;
	private String subcountryCode;
	public String getCountryID() {
		return countryID;
	}
	public void setCountryID(String countryID) {
		this.countryID = countryID;
	}
	public String getCountryCode() {
		return countryCode;
	}
	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}
	public String getCountryName() {
		return countryName;
	}
	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}
	public String getIfSet() {
		return ifSet;
	}
	public void setIfSet(String ifSet) {
		this.ifSet = ifSet;
	}
	public String getCountrySubID() {
		return countrySubID;
	}
	public void setCountrySubID(String countrySubID) {
		this.countrySubID = countrySubID;
	}
	public String getSubcountryCode() {
		return subcountryCode;
	}
	public void setSubcountryCode(String subcountryCode) {
		this.subcountryCode = subcountryCode;
	}
	public CountryAreaSetBean() {
		super();
	}
	
	
}
