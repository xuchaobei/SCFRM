package cn.gov.scciq.basicSettings.processingDegree;

public class ProcessingDegreeBean {
	private String processingDegreeID;
	private String degreeCode;
	private String degreeName;
	public ProcessingDegreeBean() {
		super();
	}
	public String getProcessingDegreeID() {
		return processingDegreeID;
	}
	public void setProcessingDegreeID(String processingDegreeID) {
		this.processingDegreeID = processingDegreeID;
	}
	public String getDegreeCode() {
		return degreeCode;
	}
	public void setDegreeCode(String degreeCode) {
		this.degreeCode = degreeCode;
	}
	public String getDegreeName() {
		return degreeName;
	}
	public void setDegreeName(String degreeName) {
		this.degreeName = degreeName;
	}
	
	
	

}
