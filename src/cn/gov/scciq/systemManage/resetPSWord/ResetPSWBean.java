package cn.gov.scciq.systemManage.resetPSWord;

public class ResetPSWBean {
	private String oldPasswords;
	private String newPasswords;
	private String entCode;
	public ResetPSWBean() {
		super();
	}
	public String getOldPasswords() {
		return oldPasswords;
	}
	public void setOldPasswords(String oldPasswords) {
		this.oldPasswords = oldPasswords;
	}
	public String getNewPasswords() {
		return newPasswords;
	}
	public void setNewPasswords(String newPasswords) {
		this.newPasswords = newPasswords;
	}
	public String getEntCode() {
		return entCode;
	}
	public void setEntCode(String entCode) {
		this.entCode = entCode;
	}
	

}
