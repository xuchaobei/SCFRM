package cn.gov.scciq.systemManage.roleManage;

public class RoleMenuLimitBean {
	
	private String menuName;
	private String operateLimit;
	private String sortNo;
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getOperateLimit() {
		return operateLimit;
	}
	public void setOperateLimit(String operateLimit) {
		this.operateLimit = operateLimit;
	}
	public String getSortNo() {
		return sortNo;
	}
	public void setSortNo(String sortNo) {
		this.sortNo = sortNo;
	}
	
}
