package cn.gov.scciq.businessProgress.checkorReceive;

public class TransDeclBean {
	
	private String declNo;					
	private String inspOrgCode	;		
	private String inspDeptCode	;		
	private String operatingOrgCode	;	
	private String operatingDeptCode;		
	private String operatingOperatorCode;
	public TransDeclBean() {
		super();
	}
	public String getDeclNo() {
		return declNo;
	}
	public void setDeclNo(String declNo) {
		this.declNo = declNo;
	}
	public String getInspOrgCode() {
		return inspOrgCode;
	}
	public void setInspOrgCode(String inspOrgCode) {
		this.inspOrgCode = inspOrgCode;
	}
	public String getInspDeptCode() {
		return inspDeptCode;
	}
	public void setInspDeptCode(String inspDeptCode) {
		this.inspDeptCode = inspDeptCode;
	}
	public String getOperatingOrgCode() {
		return operatingOrgCode;
	}
	public void setOperatingOrgCode(String operatingOrgCode) {
		this.operatingOrgCode = operatingOrgCode;
	}
	public String getOperatingDeptCode() {
		return operatingDeptCode;
	}
	public void setOperatingDeptCode(String operatingDeptCode) {
		this.operatingDeptCode = operatingDeptCode;
	}
	public String getOperatingOperatorCode() {
		return operatingOperatorCode;
	}
	public void setOperatingOperatorCode(String operatingOperatorCode) {
		this.operatingOperatorCode = operatingOperatorCode;
	}
	

}
