package cn.gov.scciq.bussiness.productCtrlItem;

public class ProductMatDetailDto {
	private String className;
	private String subclassName;
	private String materialName;
	private String sourceName;
	private String ifMainMaterial;
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getSubclassName() {
		return subclassName;
	}
	public void setSubclassName(String subclassName) {
		this.subclassName = subclassName;
	}
	public String getMaterialName() {
		return materialName;
	}
	public void setMaterialName(String materialName) {
		this.materialName = materialName;
	}
	public String getSourceName() {
		return sourceName;
	}
	public void setSourceName(String sourceName) {
		this.sourceName = sourceName;
	}
	public String getIfMainMaterial() {
		return ifMainMaterial;
	}
	public void setIfMainMaterial(String ifMainMaterial) {
		this.ifMainMaterial = ifMainMaterial;
	}
	
	
}
