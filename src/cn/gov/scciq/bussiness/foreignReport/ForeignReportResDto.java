package cn.gov.scciq.bussiness.foreignReport;

public class ForeignReportResDto {
    private String foreignReportingID;
    private String reportingTitle;
    private String countryName;
    private String entCode;
    private String entName;
    private String reportingReason;
    private String reportingDate;
    
    public String getForeignReportingID() {
        return foreignReportingID;
    }
    public void setForeignReportingID(String foreignReportingID) {
        this.foreignReportingID = foreignReportingID;
    }
    public String getReportingTitle() {
        return reportingTitle;
    }
    public void setReportingTitle(String reportingTitle) {
        this.reportingTitle = reportingTitle;
    }
    public String getCountryName() {
        return countryName;
    }
    public void setCountryName(String countryName) {
        this.countryName = countryName;
    }
    public String getEntCode() {
        return entCode;
    }
    public void setEntCode(String entCode) {
        this.entCode = entCode;
    }
    public String getEntName() {
        return entName;
    }
    public void setEntName(String entName) {
        this.entName = entName;
    }
    public String getReportingReason() {
        return reportingReason;
    }
    public void setReportingReason(String reportingReason) {
        this.reportingReason = reportingReason;
    }
    public String getReportingDate() {
        return reportingDate;
    }
    public void setReportingDate(String reportingDate) {
        this.reportingDate = reportingDate;
    }
  
    
    
}
