package cn.gov.scciq.bussiness.declQueryStatistics;

public class DeclQueryConditionDto {
	
	private String declQueryConditionID;
	private String sortNo;
	private String leftLogic;
	private String definedField;
	private String operateName;
	private String operateValue;
	private String rightLogic;
	public String getDeclQueryConditionID() {
		return declQueryConditionID;
	}
	public void setDeclQueryConditionID(String declQueryConditionID) {
		this.declQueryConditionID = declQueryConditionID;
	}
	public String getSortNo() {
		return sortNo;
	}
	public void setSortNo(String sortNo) {
		this.sortNo = sortNo;
	}
	public String getLeftLogic() {
		return leftLogic;
	}
	public void setLeftLogic(String leftLogic) {
		this.leftLogic = leftLogic;
	}
	public String getDefinedField() {
		return definedField;
	}
	public void setDefinedField(String definedField) {
		this.definedField = definedField;
	}
	public String getOperateName() {
		return operateName;
	}
	public void setOperateName(String operateName) {
		this.operateName = operateName;
	}
	public String getOperateValue() {
		return operateValue;
	}
	public void setOperateValue(String operateValue) {
		this.operateValue = operateValue;
	}
	public String getRightLogic() {
		return rightLogic;
	}
	public void setRightLogic(String rightLogic) {
		this.rightLogic = rightLogic;
	}
	
	
}
