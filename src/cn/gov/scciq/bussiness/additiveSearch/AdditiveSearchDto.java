package cn.gov.scciq.bussiness.additiveSearch;

public class AdditiveSearchDto {
	
	private String additiveName;
    private String entName;
    private String productCode;
    private String entProductName;
    private String purpose;
    private String useQuantity;
	private String countryName;
	

	public String getAdditiveName() {
		return additiveName;
	}
	public void setAdditiveName(String additiveName) {
		this.additiveName = additiveName;
	}
	public String getEntName() {
		return entName;
	}
	public void setEntName(String entName) {
		this.entName = entName;
	}
	public String getProductCode() {
		return productCode;
	}
	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}
	public String getEntProductName() {
		return entProductName;
	}
	public void setEntProductName(String entProductName) {
		this.entProductName = entProductName;
	}
	public String getAccessoryUse() {
		return purpose;
	}
	public void setAccessoryUse(String accessoryUse) {
		this.purpose = accessoryUse;
	}
	public String getCountryName() {
		return countryName;
	}
	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}
	public String getPurpose() {
		return purpose;
	}
	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}
	public String getUseQuantity() {
		return useQuantity;
	}
	public void setUseQuantity(String useQuantity) {
		this.useQuantity = useQuantity;
	}
	
	
}
