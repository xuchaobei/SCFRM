package cn.gov.scciq.bussiness.sampleRegister;

public class LabSampleInfoDto {
	
	private String sampleName;
	private String copyCount;
	private String sampleCount;
	private String countUnit;
	private String sampleRemarks;
	public String getSampleName() {
		return sampleName;
	}
	public void setSampleName(String sampleName) {
		this.sampleName = sampleName;
	}
	public String getCopyCount() {
		return copyCount;
	}
	public void setCopyCount(String copyCount) {
		this.copyCount = copyCount;
	}
	public String getSampleCount() {
		return sampleCount;
	}
	public void setSampleCount(String sampleCount) {
		this.sampleCount = sampleCount;
	}
	public String getCountUnit() {
		return countUnit;
	}
	public void setCountUnit(String countUnit) {
		this.countUnit = countUnit;
	}
	public String getSampleRemarks() {
		return sampleRemarks;
	}
	public void setSampleRemarks(String sampleRemarks) {
		this.sampleRemarks = sampleRemarks;
	}
	
	
	
}
