package cn.gov.scciq.bussiness.sampleRegister;

public class LabItemDto {
	
	private String testItem;
	private String itemName;
	private String standardName;
	private String deptSimpleName;
	private String productCategoryName;
	private String testPeriod;
	private String testFee;
	public String getTestItem() {
		return testItem;
	}
	public void setTestItem(String testItem) {
		this.testItem = testItem;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getStandardName() {
		return standardName;
	}
	public void setStandardName(String standardName) {
		this.standardName = standardName;
	}
	public String getDeptSimpleName() {
		return deptSimpleName;
	}
	public void setDeptSimpleName(String deptSimpleName) {
		this.deptSimpleName = deptSimpleName;
	}
	public String getProductCategoryName() {
		return productCategoryName;
	}
	public void setProductCategoryName(String productCategoryName) {
		this.productCategoryName = productCategoryName;
	}
	public String getTestPeriod() {
		return testPeriod;
	}
	public void setTestPeriod(String testPeriod) {
		this.testPeriod = testPeriod;
	}
	public String getTestFee() {
		return testFee;
	}
	public void setTestFee(String testFee) {
		this.testFee = testFee;
	}
	
	
}
