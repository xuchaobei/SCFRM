package cn.gov.scciq.bussiness.sampleRegister;

public class DeclProductDto {

	private String declProductDetailID;
	private String productCode;
	private String productName;
	private String baseName;
	private String ifSampling;
	private String weight;
	private String values_USD;
	public String getDeclProductDetailID() {
		return declProductDetailID;
	}
	public void setDeclProductDetailID(String declProductDetailID) {
		this.declProductDetailID = declProductDetailID;
	}
	public String getProductCode() {
		return productCode;
	}
	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getBaseName() {
		return baseName;
	}
	public void setBaseName(String baseName) {
		this.baseName = baseName;
	}
	public String getIfSampling() {
		return ifSampling;
	}
	public void setIfSampling(String ifSampling) {
		this.ifSampling = ifSampling;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getValues_USD() {
		return values_USD;
	}
	public void setValues_USD(String values_USD) {
		this.values_USD = values_USD;
	}
	
	
}
